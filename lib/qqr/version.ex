  def get(number) when number >= 1 and number <= 40 do
    {info_bits, alignment, ec_levels} = Map.fetch!(@versions, number)

    %__MODULE__{
      number: number,
      info_bits: info_bits,
      alignment_pattern_centers: alignment,
      error_correction_levels:
        Enum.map(ec_levels, fn {ec, blocks} ->
          %__MODULE__.ECLevel{ec_codewords_per_block: ec, ec_blocks: blocks}
        end)
    }
  end

  def alignment_pattern_centers(number) when number >= 1 and number <= 40 do
    {_info_bits, centers, _ec} = Map.fetch!(@versions, number)
    centers
  end

  def info_bits(number) when number >= 7 and number <= 40 do
    {info_bits, _centers, _ec} = Map.fetch!(@versions, number)
    info_bits
  end

  def info_bits(_number), do: nil

  @base_size 17
  @size_increment 4

  def base_size, do: @base_size
  def size_increment, do: @size_increment

  def dimension(%__MODULE__{number: n}), do: @base_size + @size_increment * n
  def dimension(number) when is_integer(number), do: @base_size + @size_increment * number
end
